using Xunit;

namespace unit_testing
{
    // Facts are tests which are always true. 
    // They test invariant conditions.
    public class Facts
    {
        [Fact]
        public void PassingTest()
        {
            Assert.Equal(4, Add(2, 2));
        }

        [Fact]
        public void FailingTest()
        {
            Assert.Equal(5, Add(2, 2));
        }

        int Add(int x, int y)
        {
            return x + y;
        }
    }
}
